	private BlockDataFactory blockDataFactory;
	private TileEntityFactory tileEntityFactory;
	private final short blockStateID;
	private final float toughness;
	private final float explosionResistance;
	private final byte colorID;
	
	public BlockType(NamespacedKey key, NamespacedKey clientKey, int id, short blockStateID, float toughness, float explosionResistance, BlockDataFactory blockDataFactory, TileEntityFactory tileFactory, byte colorID) {
		super(key, clientKey, id);
		this.blockStateID = blockStateID;
		this.toughness = toughness;
		this.explosionResistance = explosionResistance;
		this.colorID = colorID;
	}
	
	public BlockType(ConfigurationSection cfg) {
		super(cfg);
		this.blockStateID = (short) cfg.getInt("blockStateID");
		this.toughness = cfg.getFloat("toughness");
		this.explosionResistance = cfg.getFloat("explosionResistance");
		this.colorID = (byte) cfg.getInt("colorID");
		String tileKey = cfg.getString("tileEntityFactory", "");
		Registry<TileEntityFactory> tileFactory = Registries.getRegistry(TileEntityFactory.class);
		this.tileEntityFactory = tileFactory.getOrDefault(tileKey);
		String blockKey = cfg.getString("blockDataFactory", "");
		Registry<BlockDataFactory> blockFactory = Registries.getRegistry(BlockDataFactory.class);
		this.blockDataFactory = blockFactory.getOrDefault(blockKey);
	}
	
	/**
	 * Returns the colorID corresponding to {@link MapColor#byID(byte)}
	 * @return id
	 */
	public byte getColorID() {
		return colorID;
	}
	
	/**
	 * Returns the lowest block state id
	 * @return id
	 */
	public short getBlockStateID() {
		return blockStateID;
	}
	
	public float getToughness() {
		return toughness;
	}
	
	public float getExplosionResistance() {
		return explosionResistance;
	}
	
	public BlockDataFactory getBlockDataFactory() {
		return blockDataFactory;
	}
	
	@NotNull
	public void setBlockDataFactory(BlockDataFactory factory) {
		if (factory == null) {
			blockDataFactory = Registries.getDefault(BlockDataFactory.class);
		} else {
			blockDataFactory = factory;
		}
	}
	
	@Nullable
	public TileEntityFactory getTileEntityFactory() {
		return tileEntityFactory;
	}
	
	public void setTileEntityFactory(TileEntityFactory factory) {
		this.tileEntityFactory = factory;
	}
	
	@NotNull
	public BlockData createBlockData() {
		return blockDataFactory.createData(this);
	}
	
	@Nullable
	public TileEntity createTileEntity() {
		TileEntityFactory tf = getTileEntityFactory();
		if (tf != null)
			return tf.createTile(this);
		return null;
	}
	
	/**
	 * Returns the id used to identify the tile or -1 if no {@link TileEntityFactory} is set
	 * @return
	 */
	public int getTileID() {
		return tileEntityFactory != null ? tileEntityFactory.getTileID() : -1;
	}
	
	public boolean isValidData(BlockData data) {
		return blockDataFactory.isValidData(data);
	}
	
	public boolean isValidTile(TileEntity tile) {
		TileEntityFactory tf = getTileEntityFactory();
		return tf != null && tf.isValidTile(tile);
	}
	
	public static BlockType get(CharSequence key) {
		return getRegistry().get(key);
	}
	
	public static BlockType getByID(int id) {
		return getRegistry().getByID(id);
	}
	
	public static ProtocolRegistry<BlockType> getRegistry() {
		return REGISTRY_KEY.getRegistry();
	}

