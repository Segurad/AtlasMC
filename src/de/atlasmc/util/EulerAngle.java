package de.atlasmc.util;

public class EulerAngle {

	private double x, y, z;
	
	public EulerAngle(double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public double getZ() {
		return z;
	}

}
